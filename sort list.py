#Create a function that takes a list of strings and returns the list sorted alphabetically"

def sort_list_alphabetically(string_list):
    return sorted(string_list)

string_list = ['apple' , 'mango' , 'watermelon' , 'papaya' , 'banana' , 'orange']
sorted_list = sort_list_alphabetically(string_list)
print("Sorted list of strings:", sorted_list)



